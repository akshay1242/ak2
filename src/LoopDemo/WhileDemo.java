package LoopDemo;

public class WhileDemo {
	public static void main(String[]args) {
		//decrement
		int a=15;
		while(a>=1) {
			System.out.println(a);
			a--;
			
		}
		System.out.println("chain break");
		
		int b=1;
		while (b<=15) {
			System.out.println(b);
			b++;
			
		}
	}

}
