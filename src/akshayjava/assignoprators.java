package akshayjava;

public class assignoprators {

	public static void main(String[] args) {
		int a=10;
		int B;
		B=a;
		System.out.println("B using :"+B);// Assign
		B+=a;
		System.out.println("B using :"+B);// Add And Assign
		B-=a;
		System.out.println("B using :"+B);// Subtract and assign
		B*=a;
		System.out.println("B using :"+B);// MULTIPLICATION AND ASSIGN
		B/=a;
		System.out.println("B using :"+B);// divide AND ASSIGN
		B%=a;
		System.out.println("B using :"+B);// Modulo AND ASSIGN


	}

}
